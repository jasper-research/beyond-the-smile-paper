"""Hybrid eval: per (snapshot, tenor) cell, use smile refit when the tenor row
has ≥3 observed cells; otherwise fall back to the VAE.

The refit is essentially perfect when a tenor row is well-observed (the gridded
targets were themselves generated by the same quadratic smile family). The VAE
shines when a row loses too many cells for the smile to fit. The hybrid takes
both at face value: route each cell to whichever model is structurally
qualified for it. No re-training — just an inference-time policy.

Usage:
  python -m src.eval_hybrid              # uses best-val-loss VAE
  python -m src.eval_hybrid <vae_run>    # uses a specific source run

Writes runs/vae_btc/<stamp>_hybrid_<source>/ with config.json + metrics.json +
rmse_vs_mask.png so compare_runs picks it up uniformly.
"""

from __future__ import annotations

import argparse
import json
import sys
from datetime import datetime
from pathlib import Path

import numpy as np
import torch

from .baseline_smile import _load_k_grid, predict_one
from .config import RUNS_ROOT, TrainConfig, list_runs, load_config, save_config
from .data_btc import N_CELLS, N_DELTAS, N_TENORS, load_split
from .eval_vae import _build_masks, _device, _rmse, compute_baseline, vae_predict
from .model_vae import MaskedVAE  # noqa: F401
from .train_vae import build_model_from_ckpt

MIN_OBS_FOR_REFIT = 3  # same threshold baseline_smile uses to fit a row


def _best_vae_run() -> Path:
    """Pick the run with the lowest val_loss in its checkpoint."""
    candidates = []
    for rd in list_runs():
        ckpt = rd / "best.pt"
        if not ckpt.exists():
            continue
        try:
            blob = torch.load(ckpt, map_location="cpu", weights_only=True)
            candidates.append((float(blob["val_loss"]), rd))
        except Exception:
            continue
    if not candidates:
        raise SystemExit("no trained VAE runs found")
    candidates.sort()
    return candidates[0][1]


def hybrid_predict(
    surfaces: np.ndarray,        # (N, 6, 7) iv ground truth (observed cells used)
    k_grid: np.ndarray,          # (N, 6, 7)
    masks_flat: np.ndarray,      # (N, 42)  1=observed, 0=hidden
    fallback: np.ndarray,        # (6, 7) train-mean iv
    vae_recon_flat: np.ndarray,  # (N, 42) VAE prediction in iv units
) -> np.ndarray:
    """Return (N, 6, 7) predictions: refit where tenor row has ≥3 obs, else VAE.

    Predictions only matter at hidden cells; observed cells get the ground
    truth back so downstream RMSE math is clean.
    """
    n = len(surfaces)
    out = surfaces.copy()
    masks = masks_flat.reshape(n, N_TENORS, N_DELTAS)
    vae_recon = vae_recon_flat.reshape(n, N_TENORS, N_DELTAS)

    for i in range(n):
        mask = masks[i]
        refit_pred = predict_one(surfaces[i], k_grid[i], mask, fallback)  # NaN on observed
        obs_per_row = (mask >= 0.5).sum(axis=1)  # (6,)
        for r in range(N_TENORS):
            for c in range(N_DELTAS):
                if mask[r, c] >= 0.5:
                    continue
                if obs_per_row[r] >= MIN_OBS_FOR_REFIT and np.isfinite(refit_pred[r, c]):
                    out[i, r, c] = refit_pred[r, c]
                else:
                    out[i, r, c] = vae_recon[i, r, c]
    return out


def evaluate(source_run: Path) -> Path:
    src_cfg = load_config(source_run)
    ckpt = torch.load(source_run / "best.pt", map_location="cpu", weights_only=True)
    device = _device()
    model = build_model_from_ckpt(ckpt, src_cfg).to(device)
    model.load_state_dict(ckpt["model"])

    bundle = load_split("test")
    train_bundle = load_split("train")
    mean, std = train_bundle.mean.reshape(-1), train_bundle.std.reshape(-1)
    fallback = train_bundle.surfaces.mean(axis=0)
    surfaces = bundle.surfaces
    surfaces_flat = surfaces.reshape(-1, N_CELLS)
    surfaces_z = ((surfaces_flat - mean) / std).astype(np.float32)

    k_all, ts_all = _load_k_grid()
    ts_to_k = {t: k_all[i] for i, t in enumerate(ts_all)}
    k_split = np.stack([ts_to_k[t] for t in bundle.ts], axis=0)

    # Mirror the source VAE's config so compare_runs labels it sensibly,
    # and so the hybrid uses the SAME masks the VAE was evaluated on.
    hybrid_cfg = TrainConfig(
        name=f"hybrid_z{src_cfg.z_dim}h{src_cfg.hidden}",
        z_dim=src_cfg.z_dim, hidden=src_cfg.hidden,
        eval_mask_rates=src_cfg.eval_mask_rates,
        eval_seed=src_cfg.eval_seed,
        notes=(f"Per (snap, tenor): smile refit if ≥{MIN_OBS_FOR_REFIT} obs in "
               f"row, else VAE from {source_run.name}. Inference policy, no "
               f"new training."),
    )
    stamp = datetime.now().strftime("%Y%m%d-%H%M%S")
    out_dir = RUNS_ROOT / f"{stamp}_{hybrid_cfg.name}_from_{source_run.name[16:]}"
    out_dir.mkdir(parents=True, exist_ok=False)
    save_config(hybrid_cfg, out_dir)

    rng = np.random.default_rng(hybrid_cfg.eval_seed)
    hybrid_rmse: dict[float, float] = {}
    for rate in hybrid_cfg.eval_mask_rates:
        masks_flat = _build_masks(len(surfaces), rate, rng)
        recon_z = vae_predict(model, surfaces_z, masks_flat, device)
        vae_recon = (recon_z * std + mean).astype(np.float32)
        hybrid = hybrid_predict(surfaces, k_split, masks_flat, fallback, vae_recon)
        hidden = masks_flat < 0.5
        hybrid_rmse[rate] = _rmse(hybrid.reshape(-1, N_CELLS), surfaces_flat, hidden)

    base_rmse = compute_baseline(hybrid_cfg.eval_mask_rates, hybrid_cfg.eval_seed)
    metrics = {
        "source_vae_run": source_run.name,
        "ckpt_epoch": int(ckpt["epoch"]),
        "ckpt_val_loss": float(ckpt["val_loss"]),
        "mask_rates": list(hybrid_cfg.eval_mask_rates),
        # 'vae_rmse_vol' key is reused for compare_runs compatibility — it's
        # the headline curve for this run, which happens to be the hybrid.
        "vae_rmse_vol": {str(r): hybrid_rmse[r] for r in hybrid_cfg.eval_mask_rates},
        "baseline_rmse_vol": {str(r): base_rmse[r] for r in hybrid_cfg.eval_mask_rates},
    }
    (out_dir / "metrics.json").write_text(json.dumps(metrics, indent=2))
    _plot(hybrid_cfg, hybrid_rmse, base_rmse, source_run, out_dir)

    print(f"\n[{out_dir.name}]  source = {source_run.name}")
    print(f"  {'rate':>5s}  {'hybrid':>8s}  {'baseline':>8s}")
    for r in hybrid_cfg.eval_mask_rates:
        print(f"  {r:>5.2f}  {hybrid_rmse[r]:>8.4f}  {base_rmse[r]:>8.4f}")
    return out_dir


def _plot(cfg: TrainConfig, hybrid_rmse, base_rmse, source_run: Path, out_dir: Path) -> None:
    import matplotlib.pyplot as plt

    # Also load the VAE-only curve for the same source, for context.
    src_metrics = json.loads((source_run / "metrics.json").read_text())
    vae_only = {float(k): v for k, v in src_metrics["vae_rmse_vol"].items()}

    rates = sorted(hybrid_rmse)
    fig, ax = plt.subplots(figsize=(7.5, 4.8))
    ax.plot([r * 100 for r in rates], [vae_only[r] * 100 for r in rates],
            "o-", label=f"VAE only ({source_run.name[16:]})", linewidth=1.5, alpha=0.7)
    ax.plot([r * 100 for r in rates], [base_rmse[r] * 100 for r in rates],
            "s-", label="Smile refit only", linewidth=1.5, alpha=0.7)
    ax.plot([r * 100 for r in rates], [hybrid_rmse[r] * 100 for r in rates],
            "D-", label="Hybrid (refit when row ≥3 obs, else VAE)",
            linewidth=2.5, color="tab:red")
    ax.set_xlabel("Mask rate (% cells hidden)")
    ax.set_ylabel("RMSE on hidden cells (vol pts)")
    ax.set_title(f"BTC completion — hybrid policy vs components")
    ax.grid(alpha=0.3)
    ax.legend()
    fig.tight_layout()
    fig.savefig(out_dir / "rmse_vs_mask.png", dpi=140)
    plt.close(fig)


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("source_run", nargs="?",
                    help="path to source VAE run dir; default = best by val_loss")
    args = ap.parse_args()
    if args.source_run:
        src = Path(args.source_run)
        if not src.is_absolute():
            src = RUNS_ROOT / args.source_run
        if not src.exists():
            print(f"run dir not found: {src}", file=sys.stderr)
            sys.exit(1)
    else:
        src = _best_vae_run()
        print(f"auto-selected best VAE run: {src.name}")
    evaluate(src)


if __name__ == "__main__":
    main()
